'use strict';

/**
 * Sum two numbers.
 * @param x
 * @param y
 */
const sum = (x, y) => x + y;
/**
 * Subtract two numbers.
 * @param x
 * @param y
 */
const subtract = (x, y) => x - y;
/**
 * Divide two numbers.
 * @param x
 * @param y
 */
const divide = (x, y) => x / y;
/**
 * Multiply two numbers.
 * @param x
 * @param y
 */
const multiply = (x, y) => x * y;

module.exports = {
    sum,
    subtract,
    divide,
    multiply
};