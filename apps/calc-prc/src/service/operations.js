'use strict';

/**
 * Sum two numbers.
 * @param a
 * @param b
 */
export const sum = (a, b) => a + b;
/**
 * Subtract two numbers.
 * @param a
 * @param b
 */
export const subtract = (a, b) => a - b;
/**
 * Divide two numbers.
 * @param a
 * @param b
 */
export const divide = (a, b) => a / b;
/**
 * Multiply two numbers.
 * @param a
 * @param b
 */
export const multiply = (a, b) => a * b;